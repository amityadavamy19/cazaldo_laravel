										<h4 class="about-heading why-gt-sub-heading">
	What is E-commerce?</h4>
<p>
	All the commercial activities such as the exchange of goods and money over the internet are termed as E-commerce. Some big reputed companies operating in India are Flipkart, Amazon, Shop clue, etc.</p>
<h4 class="about-heading why-gt-sub-heading">
	What do E-commerce Companies do?</h4>
<p>
	These companies neither manufacture the products nor trade them. They provide platforms where buyers and sellers come together to fulfill each other&rsquo;s requirements. For this robust platform, e-commerce companies collect the commission on every order from the seller.</p>
<h4 class="about-heading why-gt-sub-heading">
	How does E-commerce platform work for the Market?</h4>
<p>
	E-commerce companies have two portals i) buyer portal and ii) seller portal.</p>
<ol>
	<li>
		<strong>Buyer portal:</strong>&nbsp;on this portal buyer views the product, add the product in the cart, place the order, and make the payment.</li>
	<li>
		<strong>Seller Portal:</strong>&nbsp;on this portal sellers receive orders and execute them.</li>
</ol>
<p>
	Every seller registers an e-shop on the seller portal by filling up the registration forms and submitting necessary documents like Aadhar, Pan Card, etc. Upon successful registration, sellers list their products in their e-shop. The listing goes live in the buyers portal, where a buyer can place the order after approval of the listing from the e-commerce company.</p>
<p>
	Once the order is assigned to a Seller, it&rsquo;s seller responsibility to execute the order by managing the product (manufacturing/ stock maintenance/new purchase/ etc), pack it, and handover to e-commerce logistics in the stipulated time frame (normally 2 days). Sellers are given the option to ship the material by the means of their logistics (i.e leading courier services) or the seller can opt for the services from E-commerce logistic partner.</p>
<p>
	Every e-commerce company follows a minimum 15-day replacement/ return policy for buyer&rsquo;s satisfaction. The same needs to be managed by Seller in co-ordination with logistic partners if the case comes. (If the seller decides to cancel the order or delays the shipment they get penalized heavily and same is payable by the seller)</p>
<p>
	Once the buyer receives the product and the payment is done at the time of ordering or COD, E-commerce Company pays to the seller in their bank account as per their payment schedule.</p>
<p>
	There is a possibility that the buyer may not available at the delivery location at the time of delivery, the product could not be delivered or the buyer cancels the order or e-commerce company finds that buyer information is wrong, in such a situation, the order is cancelled. All these cases are termed as cancellation.</p>
<p>
	Cancellation if done after the product is shipped, it is termed as a return. As per logistic partner return policy, the returned product is handed over to the seller (leading e-commerce company follow 30-day return product policy). The seller has to manage all the returns by themselves in close co-ordination with the e-commerce logistics company.</p>
