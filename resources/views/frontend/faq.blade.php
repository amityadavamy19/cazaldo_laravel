<h4 class="about-heading why-gt-sub-heading">
	What is settlement value/final value?</h4>
<p>
	E-commerce sites charge some commission, shipment charges, and taxes. The consolidated charge range from 25 percent to 45 percent of product listing cost. The actual charge is calculated based on the product and its category. For more details please refer e-commerce sites seller support site.</p>
<p>
	For example, a product ABC is having MRP: INR 130/-, we list it at INR 125/-. The E-commerce company will retain their charges INR 25/- and pay seller INR 100/- including TDS. This INR 100/- is the settlement value/ final value. We make the invoice considering this settlement value.</p>
