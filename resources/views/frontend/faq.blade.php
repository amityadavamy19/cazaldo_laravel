<p>
	Out of 4, just 1 book came for a replacement, therefore add 14 more days for replacement cycle payout (i.e. buyer to seller and then seller to buyer). Therefore payout will qualify on August 9th, for this one book.</p>
<p>
	<strong>Possibility 3, Return:-</strong></p>
<p>
	Balance 3 books got returned. As stated earlier on the 16th of the following calendar month of order month, we will credit back the product value in the Zoho invoice e-wallet. i.e. in the above case on 16th August, the credit amount will be reflected in your e-wallet (The said solution is provided to sellers who opt for Dropshipping only).</p>
<p>
